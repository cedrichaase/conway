
/**
 * Generic 2D Number Vector Class
 */

class Vector2D {

    /**
     * x coordinate
     */
    public x: number;

    /**
     * y coordinate
     */
    public y: number;

    /**
     * The constructor
     *
     * @param x
     * @param y
     */
    constructor(x: number, y: number) {
        this.x = x;
        this.y = y;
    }
}
